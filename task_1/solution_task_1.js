const data = ['1.5', '3', '6', '1.5'];

function toPercentages(arr) {
  const sum = arr.reduce((acc, val) => acc + parseFloat(val), 0);
  return arr.map((val) => ((parseFloat(val) / sum) * 100).toFixed(3));
}

console.log(toPercentages(data));

// Вычислительная сложность этого алгоритма равна O(n), где n - размер входного массива. Использование памяти также равно O(n), так как нам нужно сохранить только входной массив и сумму его элементов.
// Этот алгоритм должен выполняться достаточно быстро для входных массивов, содержащих до нескольких тысяч элементов. Однако для очень больших массивов время выполнения может превышать 5-секундный предел.
